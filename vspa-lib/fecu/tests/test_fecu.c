// ---------------------------------------------------------------------------
// Run one test case.
//   1. Copy input vector into aligned DMEM encode-input buffer.
//   2. Encode ENC_IN_BUF -> ENC_OUT_BUF.
//   3. Decode ENC_OUT_BUF -> DEC_OUT_BUF.
//   4. Compare DEC_OUT_BUF against reference (ceil(data_bits / 32) words).
//   Returns 0 = pass, 1 = fail.
// ---------------------------------------------------------------------------
static int run_tc(uint32_t tc_id)
{
    const TcDesc      *td        = &TC_TABLE[tc_id];
    const fecuP2pCfgT *cfg       = td->cfg;
    uint32_t           cmp_words = (cfg->data_bits + 31U) / 32U;
    uint32_t           i;
    bool               ldpc_err;

    // Clear output buffers to prevent stale data from a previous TC
    for (i = 0; i < DMEM_BUF_WORDS; i++) {
        DEC_OUT_BUF[i] = 0U;
        ENC_OUT_BUF[i] = 0U;
    }

    // Stage input into the DMEM encode-input buffer
    for (i = 0; i < DMEM_BUF_WORDS; i++) {
        ENC_IN_BUF[i] = td->input[i];
    }

    // Encode: packed bits into ENC_OUT_BUF
    fecu_p2p_encode(cfg, (uint32_t)ENC_IN_BUF, (uint32_t)ENC_OUT_BUF);
    fecu_p2p_wait_done();

    // -------------------------------------------------------------------
    // Stage 1: verify the encoder output against the Python reference model
    // (python/model.py via gen_vectors.py).  This is the deterministic,
    // model-checkable ground truth for the encode datapath and catches
    // encoder-stage bugs a decode round-trip could mask.
    //
    // Reproducibility of ENC_OUT_BUF by the single-subcarrier reference model
    // depends on the frequency-domain stage:
    //   - scrambler + BCC/LDPC coding : always reproduced
    //   - LDPC tone mapping (TC2)      : reproduced, incl. the dropped trailing
    //                                    partial OFDM symbol (see below)
    //   - BCC full-matrix interleave (TC0, BPSK 20 MHz: 3x16 = 48 = coded_bits)
    //                                  : reproduced
    //   - BCC frequency-domain scatter (TC1, 64-QAM 80 MHz): NOT reproduced.
    //     Here the BCC interleaver matrix is sized to a full OFDM symbol
    //     (nRow*nCol = 54*26 = 1404 bits) but only coded_bits=288 bits exist,
    //     so the hardware scatters the 288 coded bits across the 1404-tone
    //     grid and keeps only the sub-set that lands in the first 288 output
    //     positions.  This is a lossy, symbol-partial placement that the
    //     single-subcarrier model does not reproduce; the encoder-output bit
    //     check is therefore skipped for this datapath.
    bool bcc_freq_scatter = (!cfg->use_ldpc && !cfg->bypass_interleaver &&
                             cfg->mod == FECU_MT_64QAM);

    if (!bcc_freq_scatter) {
        uint32_t enc_bits = cfg->coded_bits;

        for (i = 0; i < enc_bits; i++) {
            uint32_t w = i / 32U;
            uint32_t s = i % 32U;
            uint32_t got = (ENC_OUT_BUF[w] >> s) & 1U;
            uint32_t exp = (td->enc[w] >> s) & 1U;
            if (got != exp) {
                printf("TC%u: ENC MISMATCH bit=%u word=%u got=0x%08X exp=0x%08X\n",
                       (unsigned)tc_id, (unsigned)i, (unsigned)w,
                       (unsigned)ENC_OUT_BUF[w], (unsigned)td->enc[w]);
                return 1;
            }
        }
        printf("TC%u: ENC output matches model (%u bits).\n",
               (unsigned)tc_id, (unsigned)enc_bits);
    } else {
        // Sanity check: the encode must at least have run and produced a
        // non-empty coded stream in the first output word.
        if (ENC_OUT_BUF[0] == 0U) {
            printf("TC%u: ENC produced no output for BCC frequency-domain "
                   "scatter datapath\n", (unsigned)tc_id);
            return 1;
        }
        printf("TC%u: ENC output check skipped (BCC 64-QAM frequency-domain "
               "scatter not modeled); encode completed.\n", (unsigned)tc_id);
    }

    // -------------------------------------------------------------------
    // Stage 2: encode->decode round-trip.
    //
    // The decode round-trip is only lossless / recoverable for the LDPC path
    // with the tone mapper bypassed:
    //   - TC4 (LDPC 648, bypass_interleaver): fully recoverable, checked below.
    //   - TC2 (LDPC 1944 + tone mapping): the trailing partial OFDM symbol
    //     (540 of 1944 coded bits) is dropped by the hardware tone mapper
    //     (ENC_OUT_BUF tail reads back as zero), so those bits are gone and a
    //     lossless round-trip is impossible; the bit-exact encoder match above
    //     is the definitive check.
    //   - BCC paths (TC0, TC1, TC3): the hardware Viterbi decoder does
    //     recover the encoder input exactly when the trellis is terminated
    //     (payload in encoder-input bits 0..11, bits 12..23 = 0: the encoder
    //     overwrites 12..17 with the flush state and 18..23 must be 0 to end
    //     in state 0).  The round-trip check is not wired up here yet (the
    //     TC payloads do not follow that shape), so the model-verified
    //     encoder output above (per IEEE 802.11 Sec 18.3.5.6) remains the
    //     correctness criterion.  TODO: re-enable a TC0/TC3 BCC decode
    //     round-trip with terminated payloads.

    // Skip the decode round-trip for every case except recoverable LDPC.
    if (!(cfg->use_ldpc && cfg->bypass_interleaver)) {
        return 0;
    }



    // Convert packed encoder output bits to 8-bit LLR format for the decoder.
    // di_bypass (BYPASS bit[6]) is never set, so the DMEM interface always uses
    // 8-bit LLRs (scale=8, step=8) regardless of bypass_interleaver.
    //
    // LLR sign convention is the SAME for both decoder types (verified on the
    // ISS with a clean codeword: the opposite polarity decodes to garbage):
    //   LDPC decoder:    positive input LLR = bit 1.
    //                    bit 0 -> 0xE1 (-31),  bit 1 -> 0x1F (+31).
    //   Viterbi decoder: positive input LLR = bit 1 (same polarity as LDPC).
    //                    bit 0 -> 0xE1 (-31),  bit 1 -> 0x1F (+31).
    // Unified LLR convention for all decoders (matches golden).
    bits_to_llr(ENC_OUT_BUF, LLR_BUF, cfg->coded_bits, 8U, 0xE1U, 0x1FU);

    // Decode from LLR_BUF (fecu_p2p_decode calls wait_done internally)
    KCYC_START();
    ldpc_err = fecu_p2p_decode(cfg, (uint32_t)LLR_BUF, (uint32_t)DEC_OUT_BUF);
    KCYC_STOP_PRINT();
    if (ldpc_err) {
        printf("TC%u: LDPC uncorrectable error\n", (unsigned)tc_id);
        return 1;
    }

    // Compare recovered data against reference
    for (i = 0; i < cmp_words; i++) {
        if (DEC_OUT_BUF[i] != td->ref[i]) {
            printf("TC%u: MISMATCH word=%u got=0x%08X exp=0x%08X\n",
                   (unsigned)tc_id, (unsigned)i,
                   (unsigned)DEC_OUT_BUF[i], (unsigned)td->ref[i]);
            return 1;
        }
    }
    return 0;
}

// ---------------------------------------------------------------------------
// TC5: multi-block LDPC encode reproducer 
//
// The FECU multi-block encode path (FIRST -> ONE_BEFORE_FINAL -> FINAL) drops
// info bit K-1 (=323) of the ONE_BEFORE_FINAL block when all-ones data are
// encoded with N=648, K=324, rate-1/2.  
//
// Expected result if the bug is present:
//   block 0 (FIRST)            info bit 323 = 1  PASS
//   block 1 (ONE_BEFORE_FINAL) info bit 323 = 0  FAIL
//   block 2 (FINAL)            info bit 323 = 1  PASS
//
// Expected result if the bug is absent (or fixed in the encoder implementation):

//   all three blocks: info bit 323 = 1  PASS
//
// Returns 0 if all three systematic bits are 1, 1 if any is wrong.
// ---------------------------------------------------------------------------
static int run_tc5_multiblock_ones(void)
{
    uint32_t i;
    int      fails = 0;

    // ---- fill ENC_IN_BUF with all-ones (3 x 324 info bits = 972 bits) ----
    for (i = 0; i < DMEM_BUF_WORDS; i++) {
        ENC_IN_BUF[i]  = 0xFFFFFFFFU;
        ENC_OUT_BUF[i] = 0U;
    }

    // ---- common register setup for a systematic LDPC encode -------------
    // (IEEE 802.11n LDPC, Sec 20.3.11.7; register programming per the FECU
    //  hardware manual)

    // CONFIG: encode=1, use_ldpc=1, num_streams=1, num_bcc_enc=0, BPSK/20MHz
    __ip_write(FECU_CONFIG_ADDR, FECU_CONFIG_BIT_MASK,
               FECU_CONFIG_FECU_ENC_MASK |
               FECU_CONFIG_USE_LDPC_MASK |
               (1U << FECU_CONFIG_NUM_STREAM_B));

    // SIZES: data_bits=324, coded_bits=648
    __ip_write(FECU_SIZES_ADDR, FECU_SIZES_BIT_MASK,
               (648U << 16) | 324U);

    // LDPC_CONFIG: max_iter=20, coding_rate=1/2 (0), block_length=648 (0),
    //              repeat=0 (clearing REPEAT_MASK is the fix for the first
    //              variant of the bug; its absence here is intentional to
    //              isolate the ONE_BEFORE_FINAL multi-block path issue)
    __ip_write(FECU_LDPC_CONFIG_ADDR,
               FECU_LDPC_MAX_ITER_MASK | FECU_LDPC_CODING_RATE_MASK |
               FECU_LDPC_BLOCK_LENGTH_MASK | FECU_LDPC_REPEAT_MASK,
               (20U << FECU_LDPC_MAX_ITER_B));

    __ip_write(FECU_LDPC_SIZES_ADDR,      FECU_SIZES_BIT_MASK, 0U);
    __ip_write(FECU_LDPC_EXTRA_SHORT_ADDR, 0xFFFFFFFFU, 0U);
    __ip_write(FECU_LDPC_EXTRA_REP_ADDR,  0xFFFFFFFFU, 0U);

    // BYPASS: scrambler + conv encoder + viterbi + interleaver all bypassed
    __ip_write(FECU_BYPASS_ADDR, FECU_BYPASS_REG_BIT_MASK,
               FECU_BYPASS_SC_MASK  | FECU_BYPASS_CE_MASK |
               FECU_BYPASS_VD_MASK  | FECU_BYPASS_INT_MASK);

    // BCC registers cleared (not used by LDPC path)
    __ip_write(FECU_BCC_PUNC_MASK_ADDR, 0xFFFFFFFFU, 0U);
    __ip_write(FECU_BCC_CONFIG_ADDR,    FECU_BCC_CONFIG_BIT_MASK, 0U);

    // ---- block 0: FIRST_SYMBOL ----
    // Read 324+12 bits from ENC_IN_BUF[0]; keep 12 bits for next block.
    // The encoder consumes a full short (16 bits) for the last partial group,
    // keeping the top 12 bits buffered for the next block (keep_bits=12).
    FECU_SetDmemReadCnt(324U + 12U);
    FECU_SetDmemInAddr((uint32_t)ENC_IN_BUF | (12U << 24));
    FECU_SetDmemOutAddr((uint32_t)ENC_OUT_BUF);
    FECU_SetControl(FECU_CTRL_1ST_SYM_MASK | FECU_CTRL_START_IMM);
    fecu_p2p_wait_done();

    // ---- block 1: ONE_BEFORE_FINAL_SYMBOL ----
    // Resume 12 bits into the next short; read 312 fresh info bits + 8 more
    // to fill the last short, keeping the top 8 bits for block 2.
    // Byte offset of ENC_IN_BUF short[21] = 21*2 = 42 bytes.
    // Byte offset of ENC_OUT_BUF short[42] = 42*2 = 84 bytes.
    FECU_SetDmemReadCnt(312U + 8U);
    FECU_SetDmemInAddr(((uint32_t)ENC_IN_BUF + 42U) | (8U << 24));
    FECU_SetDmemOutAddr((uint32_t)ENC_OUT_BUF + 84U);
    FECU_SetControl(FECU_CTRL_2ND_2_LAST_SYM_MASK | FECU_CTRL_START_IMM);
    fecu_p2p_wait_done();

    // ---- block 2: FINAL_SYMBOL ----
    // Resume 8 bits; read the remaining 316 info bits (no keep needed).
    // Byte offset of ENC_IN_BUF short[41] = 41*2 = 82 bytes.
    // Byte offset of ENC_OUT_BUF short[84] = 84*2 = 168 bytes.
    FECU_SetDmemReadCnt(316U);
    FECU_SetDmemInAddr((uint32_t)ENC_IN_BUF + 82U);
    FECU_SetDmemOutAddr((uint32_t)ENC_OUT_BUF + 168U);
    FECU_SetControl(FECU_CTRL_LAST_SYM_MASK | FECU_CTRL_START_IMM);
    fecu_p2p_wait_done();

    // ---- check systematic info bit 323 in each block's output ----
    // Systematic LDPC (rate 1/2): info bit i appears at output bit i.
    // Info bit 323 (K-1) in block B starts at absolute output bit B*672 + 323:
    //   block 0: bit  323 -> ENC_OUT_BUF word 10, bit 3   (byte offset   0)
    //   block 1: bit  995 -> ENC_OUT_BUF word 31, bit 3   (byte offset  84)
    //   block 2: bit 1667 -> ENC_OUT_BUF word 52, bit 3   (byte offset 168)
    //
    // 672 = 84 bytes * 8 bits/byte (output stride per block).
    // Bit 323: 0*672+323 / 32 = word 10, 323 % 32 = bit 3.
    // Bit 995: 1*672+323 / 32 = word 31, 995 % 32 = bit 3.
    // Bit 1667: 2*672+323 / 32 = word 52, 1667 % 32 = bit 3.
    {
        uint32_t b0 = (ENC_OUT_BUF[10] >> 3) & 1U;
        uint32_t b1 = (ENC_OUT_BUF[31] >> 3) & 1U;
        uint32_t b2 = (ENC_OUT_BUF[52] >> 3) & 1U;

        printf("TC5 (LDPC_issue multi-block reproducer)\n");
        printf("  block 0 (FIRST)            info bit 323: expect 1 got %u  %s\n",
               (unsigned)b0, b0 == 1U ? "PASS" : "FAIL");
        printf("  block 1 (ONE_BEFORE_FINAL) info bit 323: expect 1 got %u  %s\n",
               (unsigned)b1, b1 == 1U ? "PASS" : "FAIL");
        printf("  block 2 (FINAL)            info bit 323: expect 1 got %u  %s\n",
               (unsigned)b2, b2 == 1U ? "PASS" : "FAIL");

        if (b0 != 1U) fails++;
        if (b1 != 1U) fails++;
        if (b2 != 1U) fails++;

        // Full word-for-word comparison against the Python reference model
        // (enc_tc5.hex): 3 codewords x 648 bits at a 672-bit output stride.
        // Compare through the end of block 2 = (2*672 + 648) bits = 1992 bits
        // -> ceil(1992 / 32) = 63 words.
        {
            uint32_t w;
            for (w = 0; w < 63U; w++) {
                if (ENC_OUT_BUF[w] != ENC_TC5[w]) {
                    printf("TC5: ENC MISMATCH word=%u got=0x%08X exp=0x%08X\n",
                           (unsigned)w, (unsigned)ENC_OUT_BUF[w],
                           (unsigned)ENC_TC5[w]);
                    fails++;
                    break;
                }
            }
        }

        if (fails == 0) {
            printf("TC5: PASS (no encoder bug observed)\n");
        } else if (b0 == 1U && b1 == 0U && b2 == 1U) {
            printf("TC5: FAIL - ONE_BEFORE_FINAL drops info bit 323 "
                   "(LDPC_issue_multiblock bug reproduced)\n");
        } else {
            printf("TC5: FAIL - unexpected pattern b0=%u b1=%u b2=%u\n",
                   (unsigned)b0, (unsigned)b1, (unsigned)b2);
        }
    }
    return (fails > 0) ? 1 : 0;
}

